SLASH_IBLASTDRUID1 = "/iblast"
function SlashCmdList.IBLASTDRUID()
	IWin:InitializeRotation()
	IWin:TargetEnemy()
	IWin:UseItemTrinketOffensivePrepull(true)
	IWin:StartAttack()
	IWin:MarkOfTheWild()
	IWin:Thorns()
	IWin:CancelRoot()
	IWin:MoonkinForm()
	IWin:UseItemConsumableOffensiveNoGCDRanged()
	IWin:UseItemTrinketOffensiveNoGCDRanged()
	IWin:UseItemConsumableAOEOffensiveNoGCD(nil, nil, "meleeAutoAttack")
	IWin:UseItemConsumableAOEOffensiveGCD(nil, nil, "meleeAutoAttack")
	IWin:WrathOOC()
	IWin:StarfireOOC()
	IWin:InsectSwarm()
	IWin:Moonfire()
	IWin:Wrath()
	IWin:Starfire()
	IWin:InsectSwarmMoving()
	IWin:MoonfireMoving()
end

